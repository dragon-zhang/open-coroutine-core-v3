.file "set_x86_64_sysv_elf_gas.S"
.text
.globl setjmp
.type setjmp,@function
.align 16
setjmp:
    lea LJMPRET_LABEL(%rip), %rcx
    xor %rax, %rax
    mov %rbx, (%rdi)
    mov %rbp, 8(%rdi)
    mov %r12, 16(%rdi)
    mov %rsp, 24(%rdi)
    mov %r13, 32(%rdi)
    mov %r14, 40(%rdi)
    mov %r15, 48(%rdi)
    mov %rcx, 56(%rdi)
    mov %rsi, 64(%rdi)
    mov %rdx, 72(%rdi)
LJMPRET_LABEL:
    ret
.size setjmp,.-setjmp

/* Mark that we don't need executable stack.  */
.section .note.GNU-stack,"",%progbits
